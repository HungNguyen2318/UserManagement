package hungnm.promotion;

import java.io.Serializable;

/**
 *
 * @author SE130008
 */
public class PromoDTO implements Serializable{
    private String userId;
    private int rank;
    private String time;

    public PromoDTO() {
    }

    public PromoDTO(String userId, int rank, String time) {
        this.userId = userId;
        this.rank = rank;
        this.time = time;
    }

    /**
     * @return the userId
     */
    public String getUserId() {
        return userId;
    }

    /**
     * @param userId the userId to set
     */
    public void setUserId(String userId) {
        this.userId = userId;
    }

    /**
     * @return the rank
     */
    public int getRank() {
        return rank;
    }

    /**
     * @param rank the rank to set
     */
    public void setRank(int rank) {
        this.rank = rank;
    }

    /**
     * @return the time
     */
    public String getTime() {
        return time;
    }

    /**
     * @param time the time to set
     */
    public void setTime(String time) {
        this.time = time;
    }
    
    
}
