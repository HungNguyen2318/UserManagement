package hungnm.role;

import java.io.Serializable;

/**
 *
 * @author SE130008
 */
public class RoleDTO implements Serializable{
    private String type;
    private int id;

    public RoleDTO() {
    }

    public RoleDTO(String type, int id) {
        this.type = type;
        this.id = id;
    }

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }
    
    
}
